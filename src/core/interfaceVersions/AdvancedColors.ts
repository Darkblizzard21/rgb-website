// Decipher Color

export const GetColorName = (color: Color) : string => {
    if(color.a == 0)
        return "AirTransparent"

    let distance: number = Number.MAX_VALUE;
    let colorName: string = "";
    const checkNearer = (compareColor: Color, compareColorName: string) => {
        let compareDistance = Math.pow(color.r - compareColor.r, 2) + Math.pow(color.g - compareColor.g, 2) + Math.pow(color.b - compareColor.b, 2);
        if (compareDistance < distance) {
            distance = compareDistance;
            colorName = compareColorName;
        }
    }
    ColorMappings.forEach((element) => {checkNearer(element[0] as Color, element[1] as string)})

    return colorName;
}